// โปรแกรมนี้ใช้คำนวณพื้นที่สี่เหลี่ยมผืนผ้าจากความกว้างและความยาวที่ผู้ใช้ป้อน

// นำเข้าscannersเพื่อใช้รับข้อมูลจากkeybord
import java.util.Scanner;

// ตั้งชื่อclassให้ตรงกับชื่อfile
public class TestRec1 {
    // method main - จุดเริ่มต้นของโปรแกรม
    public static void main(String[] args) {
        // 1. สร้างตัวแปรสำหรับรับค่าจากผู้ใช้
        Scanner scanner = new Scanner(System.in);

        // 2. ประกาศตัวแปรชนิด double เพื่อเก็บความกว้าง/ความยาว/และพื้นที่
        //    ใช้ double เพราะอาจมีจุดทศนิยม
        double width;   // ความกว้าง
        double length;  // ความยาว
        double area;    // พื้นที่

        // 3. แสดงข้อความแจ้งให้ผู้ใช้ป้อนความกว้าง
        System.out.print("กรุณาป้อนความกว้าง (หน่วย: เซนติเมตร): ");
        // รับค่าความกว้างจากผู้ใช้ แล้วเก็บไว้ในตัวแปร width
        width = scanner.nextDouble();

        // 4. แสดงข้อความแจ้งให้ผู้ใช้ป้อนความยาว
        System.out.print("กรุณาป้อนความยาว (หน่วย: เซนติเมตร): ");
        // รับค่าความยาวจากผู้ใช้ แล้วเก็บไว้ในตัวแปร length
        length = scanner.nextDouble();

        // 5. คำนวณพื้นที่สี่เหลี่ยมผืนผ้า = กว้าง × ยาว
        area = width * length;

        // 6. แสดงผลลัพธ์ทางหน้าจอ พร้อมหน่วย
        System.out.println("--------------------------------");
        System.out.println("พื้นที่สี่เหลี่ยมผืนผ้า = " + area + " ตารางเซนติเมตร");
        System.out.println("--------------------------------");

        // 7. ปิด scanner เพื่อป้องกันการรั่วไหลของหน่วยความจำ (ปิดเมื่อไม่ใช้แล้ว)
        scanner.close();
    }
}